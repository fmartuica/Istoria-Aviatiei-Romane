<font size=4>
<div style="text-align: justify; text-indent: 40pt; margin: 2% 10% 0% 10%"> <b>Aurel Vlaicu</b>  a fost un inginer român, inventator și pionier al aviației române și mondiale. În cinstea lui, comuna Binținți se numește astăzi Aurel Vlaicu. </div>
<div style="text-align: justify; text-indent: 40pt; margin: 0% 10% 0% 10%"> A fost elev al liceului calvinist Kun Kocsárd din Orăștie, care din 1919 încoace a fost numit „Liceul Aurel Vlaicu”, luându-și bacalaureatul la Sibiu în 1902. </div>
<div style="text-align: justify; text-indent: 40pt; margin: 0% 10% 0% 10%">Și-a început studiile inginerești la Universitatea Tehnică din Budapesta, studii pe care le-a continuat la Technische Hochschule din München, obținând diploma de inginer în 1907. După aceea a lucrat ca inginer la uzinele Opel în Rüsselsheim. În 1908 se întoarce la Binținți unde construiește un planor cu care efectuează un număr de zboruri în 1909. În toamna anului 1909 se mută în București și începe construcția primului său avion, Vlaicu I, la Arsenalul Armatei. Avionul zboară fără modificări (lucru unic pentru începuturile aviației mondiale) în iunie 1910. În anul 1911 construiește un al doilea avion, Vlaicu II, cu care în 1912 a câștigat cinci premii memorabile (1 premiu I si 4 premii II) la mitingul aerian de la Aspern, Austria. Concursul a reunit între 23 și 30 iunie 1912, 40 piloți din 7 țări, dintre care 17 din Austro-Ungaria, 7 germani, 12 francezi printre care și Roland Garros, cel mai renumit pilot al vremii, un rus, un belgian, un persan și românul Vlaicu. În cel mai cunoscut ziar vienez, <i>Neue Freie Presse</i>, se găseau următoarele rânduri despre zborurile lui Vlaicu:  </div>
<div style="text-align: justify; text-indent: 40pt; margin: 2% 10% 0% 10%"> <i>„Minunate și curajoase zboruri a executat românul Aurel Vlaicu, pe un aeroplan original, construit chiar de zburător, cu două elici, între care șade aviatorul. De câte ori se răsucea (vira) mașina aceasta în loc, de părea că vine peste cap, lumea răsplătea pe român cu ovații furtunoase, aclamându-l cu entuziasm de neînchipuit.”</i> </div>
<div style="text-align: justify; text-indent: 40pt; margin: 2% 10% 0% 10%">  La 13 septembrie 1913, în timpul unei încercări de a traversa Munții Carpați cu avionul său Vlaicu II, s-a prăbușit în apropiere de Câmpina, se pare că din cauza unui atac de cord. Este înmormântat în cimitirul Bellu din București.</div>
<div style="text-align: justify; text-indent: 40pt; margin: 0% 10% 0% 10%"> 	În anul următor prietenii săi Magnani și Silișteanu finalizează construcția avionului Vlaicu III, și cu ajutorul pilotului Petre Macavei efectuează câteva zboruri scurte. Autoritățile vremii interzic continuarea încercărilor; în toamna anului 1916, în timpul ocupației germane, avionul este expediat la Berlin. Avionul Vlaicu III a fost văzut ultima dată în anul 1940. </div>
