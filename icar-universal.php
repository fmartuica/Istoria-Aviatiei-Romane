<h1 style="margin: 2% 10% 0% 10%">I.C.A.R.-UNIVERSAL (1934)</h1>
<h2 style="margin: 0% 10% 0% 10%">Avion de școală, antrenament și turism</h2>

<font size=4>
<div style="text-align: justify; margin: 2% 10% 0% 10%"><b> Anvergura 11,90 m, lungimea 6,90 m, înălțimea 1,95 m, suprafața portantă 14,30 m<sup>2</sup>, greutatea gol 465 kg, greutatea totală 710 kg, motorul: <i>De Havilland-Gipsy Major</i> 130 C.P., viteza maximă 195 km/oră, viteza de croazieră 160 km/oră plafonul 5.500 m, timpul de urcare până la 3.000 de m, 12 minute 30 secunde, autonomia de zbor 3 ore.</b> </div>
<div style="text-align: justify; text-indent: 40pt; margin: 2% 10% 0% 10%"> Avionul I.C.A.R.-Universal a fost folosit cu rezultate bune în cadrul școlilor civile și militare de pilotaj, în special ca avion de faza I. Calitățile de construcție și performanțele sale de zbor au fost confirmate de raidul unei formații de trei avioane efectuat, în anul 1935, pe traiectul București-Capetown (11.500 km) în timp de 73 de ore și 5 minute de zbor efectiv. Reîntoarcerea s-a făcut tot în zbor, distanța totală de aproximativ 23.000 km, fiind parcursă în 27 de zile, cu un total de 149 de ore și 10 minute de zbor. Din echipajele celor trei avioane au făcut parte: locotenent-comandor Mihail Pantazi, locotenent-comandorul Alexandru Cernescu, locotenent-comandorul Gheorghe Davidescu, căpitan-comandorul Gheorghe Jienescu, căpitanul Gheorghe Olteanu și mecanicul Anton Ștengher (care era și pilot). Pentru acest raid dificil, ținând seamă de lungimea traiectului, de dificultățile de ordin meteorologic și de configurația regiunilor ce urmau a fi survolate, au fost alese trei avioane de serie, aflate la acea dată în dotarea școlilor militare. Fabrica constructoare le-a adus câteva îmbunătățiri și amenajări, printre care modificarea rezervoarelor pentru mărirea autonomiei de zbor (de la trei ore și jumătate) și instalarea la bord a echipamentului specific pentru un raid deasupra continentului african. Deși, în urma acestui fapt, avionul și-a mărit greutatea cu peste 150 kg, calitățile lui de zbor nu s-au madificat.</div>
<div style="text-align: justify; text-indent: 40pt; margin: 0% 10% 0% 10%"> Raidul celor trei avioane I.C.A.R., prin reușita lui deplină, a constituit una din performanțele de prestigiu ale avioanelor de construcție românească.  </div>
</font>

