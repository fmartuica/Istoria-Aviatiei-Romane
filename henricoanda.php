<font size=4>
<div style="text-align: justify; text-indent: 40pt; margin: 2% 10% 0% 10%"> Născut la Bucureşti pe 7 iunie 1886, <b>Henri Coandă</b> a fost fiul generalului Constantin Coandă. A fost unul dintre cei 7 copii ai generalului, dintre care doar doi băieţi şi două fete au supraviețuit. De mic copil a fost fascinat de forţa vântului şi de ideea de zbor. </div>
<div style="text-align: justify; text-indent: 40pt; margin: 0% 10% 0% 10%"> În timpul şcolii nu a excelat. Fiind în situaţia de a rămâne corigent la matematică, a fost mutat de tatăl sau la Liceul Militar din Iaşi. În acest moment a început educaţia sa intelectuală. Sub supravegherea unor profesori ca G. Ibrăileanu sau A.D. Xenopol, tânărul Henri s-a transformat într-un elev excepţional, dezvoltând o pasiune pentru matematică. Îşi continuă studiile atât în ţară cât şi în străinătate, absolvind Şcoala Superioară de Aeronautică şi Construcţii Mecanice din Paris, ca şef de promoţie. </div>
<div style="text-align: justify; text-indent: 40pt; margin: 0% 10% 0% 10%"> Şi-a început activitatea profesională pe şantiere conduse de inginerul Gustave Eiffel, apoi se întoarce în ţară la Arsenalul Armatei Române din Bucureşti, unde a construit o serie de machete. Prezintă unul dintre proiectele sale la Salonul Internaţional de Aeronautică din Paris în 1910. A expus un avion bazat pe propulsie prin reacţie, pe care l-a testat chiar el. Tot în 1910, observă un fenomen pe care l-a brevetat abia în 1934, <b>„efectul Coandă”</b>. </div>
<div style="text-align: justify; text-indent: 40pt; margin: 0% 10% 0% 10%"> Acest fenomen este simplu:  <b>Coandă a observat alinierea apropiată de alipire a jeturilor gazelor arse față de fuzelajul avionului</b>. Aplicat la motoreactorul din 1910, doar intuitiv, acest fenomen a fost ulterior studiat cu atenţie, ducând la apariţia turboreactoarelor, la care forţa de propulsie şi portanţa sunt asigurate de jeturile de gaze evacuate cu viteză foarte mare. </div>
<div style="text-align: justify; text-indent: 40pt; margin: 0% 10% 0% 10%">	După avionul din 1910, Coandă a prezentat un alt avion, bimotor, în 1911, cu ocazia unui concurs aviatic militar. A urmat apoi seria de avioane Coandă-Bristol (construite de Uzinele Bristol), în perioada 1911-1914. În 1916 la uzinele Delauney-Belleville, construieşte un avion echipat cu două elice propulsoare, montate la extremitatea posterioară a fuzelajului. După 1945, primeşte o ofertă de la United States Corporation, pentru participarea la unele testări ce vizau aplicarea efectului Coandă la realizarea unor tehnici de zbor. În 1969 se întoarce şi rămâne definitiv în ţară.   </div>
<div style="text-align: justify; text-indent: 40pt; margin: 0% 10% 0% 10%"> Numele lui nu apare doar în analele aeronauticii. Henri Coandă a avut o carieră prolifică, concretizată în peste 250 de brevete cu aplicaţie în cele mai diverse domenii. A inventat <b>rezervorul din beton armat pentru carburanţi, tunul fără recul</b> din dotarea avioanelor militare, <b>o instalaţie de desalinizare a apei marine</b>. Printre ideile sale se numără şi aerotubexpresul, o formă de transport ce ar putea atinge viteze de 1000 km/h. Aerotubexpresul e un tren format din containere ce circula prin interiorul unor tuburi, propulsat de vid (sistemul e acelaşi ca sistemul seringilor: când blochezi vârful cu degetul şi tragi mânerul şi îi dai drumul, se mişcă rapid până la capăt).  În 1971, Henri Coandă a contribuit la constituirea <i>Institutului Naţional pentru Creaţie Ştiinţifică şi Tehnică</i> din România. </div>
<div style="text-align: justify; text-indent: 40pt; margin: 0% 10% 0% 10%">  Membru al Societăţii Regale de Aeronautică din Londra şi membru al Academiei Române, Henri Coandă a primit şi titlul de <b>Doctor Honoris Causa</b> al institutului Politehnic Bucureşti. Doctor Honoris Causa sau „doctor datorită meritelor” este un titlu onorific, care se acordă atât pentru buna reputaţie, cât şi pentru meritele profesionale.</div>
