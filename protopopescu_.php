<div style="text-align: justify; text-indent: 40pt; margin: 2% 10% 0% 10%">
Stefan Protopopescu was originally from Mehedinti. He is attracted to aviation and attends the school at the Chitila Aeronautical Complex (near Bucharest), founded on 20 November 1909. 
Here, on July 9, 1911, the second 2nd lieutenant Stefan Protopopescu obtained the military pilot's license, becoming the first patented pilot in Romania and the first pilot of the Romanian army.
The patent flight was made with a 1909 model Farman III, built under license in the Chitila workshops.
 Immediately after him, on July 17,  obtains the pilot patent and his colleague,2nd lieutenant Gheorghe Negrescu. In the years 1913-1914 both will study at the Upper School of Mechanical and Aeronautical Constructions in Paris, becoming the first military aviation engineers of the Romanian Army.</div>
<div style="text-align: justify; text-indent: 40pt; margin: 0% 10% 0% 10%">
On July 1, 1920, the Aeronautical Arsenal was established, headed by major aviator engineer Stefan Protopopescu.After the project, he built the plane Proto, which was to be the first Romanian aircraft built in 1922 in a Romanian industrial unit. </div>
<div style="text-align: justify; text-indent: 40pt; margin: 0% 10% 0% 10%">
In 1923 at the Astra factory in Arad, after another project, 25 copies of Proto-1 were made.After the project, the Proto was constructed, which was to be the first domestic aircraft built in a Romanian industrial unit in the year 1922. </div>
<div style="text-align: justify; text-indent: 40pt; margin: 0% 10% 0% 10%">In The year 1923, at the Astra factory in Arad, after another project, 25 copies of the Proto-1 apparatus are carried out.  Following an accident during the trials, the accident in which the factory test pilot, Ion Sava, one of the best pilots of the First World War, was killed, the planes are strengthened, becoming Proto 2. They were used as school planes for phase II, which corresponded to obtaining the international flight patent.In The year 1924 it is founded in Bucharest the Company for technical exploitation (S.E.T.), where according to Stefan Protopopescu plans, the Proto-3 construction runs.Also there are two construction of the recognition biplane Proto-S.E.T., after a project of his in collaboration with the engineer Dumitru Baziliu. </div>
<div style="text-align: justify; text-indent: 40pt; margin: 0% 10% 0% 10%"> In 1924, at the request of the Romanian Government, he prepared a memo on the establishment of an aeroplane and engine factory, which Was supported by chief of Staff, Colonel Paul Teodorescu. Following this action, the Romanian Parliament adopted in the year 1925 "The Law on industrial enterprises in connection with national defence", the Birth act of the Aircraft factory which was to bear the name of the Aeronautical Industry Romanian (IAR). </div>

