<h1 style="margin: 2% 10% 0% 10%">R.G.-5 PESCĂRUȘ (1957)</h1>
<h2 style="margin: 0% 10% 0% 10%">Planor de performanță</h2>

<font size=4>
<div style="text-align: justify; margin: 2% 10% 0% 10%"><b>Anvergura 15,10 m, lungimea 7,38 m, înălțimea 2,15 m, suprafața portantă 17,28 m<sup>2</sup>, greutatea gol 210 kg, greutatea totală 300 kg, încărcarea 19,50 kg/m<sup>2</sup>, finețea maximă 26,00, viteza maximă 180 km/oră, viteza maximă admisă: în picaj 210 km/oră; la remorcaj (avion) 120 km/oră, viteza minimă 50 km/oră, viteza verticală de coborâre (minimă) 0,76 m/s. </b></div>
<div style="text-align: justify; text-indent: 40pt; margin: 2% 10% 0% 10%">Calitățile aerodinamice bune ale planorului  R.G.-5 și, în special, sensibilitatea la comenzi și viteza verticală de coborâre mică permiteau ca el să poată fi menținut ușor în zborul la pantă și în curenți termici slabi.  </div>
<div style="text-align: justify; text-indent: 40pt; margin: 0% 10% 0% 10%"> În anul 1958, planorista <i>Aurelia Roșianu-Gheorghiu</i> a stabilit cu un planor R.G.-5 recordul național feminin de viteză pe un traiect de 100 km, cu țel fix, pentru planoarele din clasa D1, cu o performanță de 51,200 km/oră.</div>
<div style="text-align: justify; text-indent: 40pt; margin: 0% 10% 0% 10%"> Planorul monoloc R.G.-5 a fost construit la <i>Întreprinderea forestieră de industrializare a lemnului</i> (I.F.I.L) din Reghin, după proiectul lui Vladimir Novițchi.</div>
