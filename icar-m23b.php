<h1 style="margin: 2% 10% 0% 10%">I.C.A.R.-M 23b (1932)</h1>
<h2 style="margin: 0% 10% 0% 10%">Avion de școală și turism</h2>

<font size=4>
<div style="text-align: justify; margin: 2% 10% 0% 10%"><b>Anvergura 11,50 m, lungimea 6,50 m,  înălțimea 2,00 m, greutatea gol 375 kg, greutatea totală 700 kg, motorul: <i>Siemens Halske SH 13b</i> 80 C.P., viteza maximă 170 km/oră, viteza de croazieră 150 km/oră, viteza minimă 80 km/oră, plafonul 3.800 m, autonomia de zbor 5 ore 30 mminute. </b></div>
<div style="text-align: justify; text-indent: 40pt; margin: 2% 10% 0% 10%">Este primul avion construit în cadrul fabricii <b>I.C.A.R.</b> din București, după o licență a avionului <b>Messerschmitt M-23</b>. Avea un schelet de lemn acoperit parțial cu placaj, parțial cu pânză. </div>
<div style="text-align: justify; text-indent: 40pt; margin: 0% 10% 0% 10%">Avionul I.C.A.R.-M 23b a fost foarte mult folosit în școlile civile de pilotaj din țara noastră. De asemenea, acest avion era nelipsit din programul mitingurilor aeronautice din acea vreme. Populara <b>„Escadrilă argintie”</b>, cum era denumită formația de avioane I.C.A.R.-M 23b a asociației <b>A.R.P.A.</b>,  a executat la aceste mitinguri nenumărate zboruri de agrement cu cei dornici să primească „botezul aerului”. Datorită construcției sale reușite și calităților sale de zbor, cu avionul I.C.A.R.-M 23b au fost realizate două performanțe deosebite. </div>
<div style="text-align: justify; text-indent: 40pt; margin: 0% 10% 0% 10%"> În anul 1932, <b>Mihail Pantazi</b> și <b>Gheorghe Grozea</b>, pilotând un avion I.C.A.R.-M 23b echipat cu flotoare și rezervoare suplimentare, au stabilit un record mondial de durată pentru hidroavioane ușoare cu timpul de 12 ore și 2 minute.</div>
<div style="text-align: justify; text-indent: 40pt; margin: 0% 10% 0% 10%"> În anul 1933, <b>Mihail Pantazi</b>, <b>Gheorghe Davidescu</b>, <b>Alexandru Cernescu</b>, <b>Petre Ivanovici</b>, <b>Max Manolescu</b> și mecanicul <b>D. Ploeșteanu</b>, formând echipajele a trei avioane I.C.A.R.-M 23b, au realizat primul raid intercontinental al aviației noastre, zburând pe distnța București-Malakal (Sudan). Deși inițial se preconizase ca punctul final al raidului să fie Capetown, zborul a fost întrerupt la Malakal, din cauza condițiilor meteorologice neprielnice și a terenurilor de aterizare devenite impracticabile în urma ploilor. Acest raid a confirmat calitățile materialului volant, care - în condițiile meteorologice specifice continentului african - s-a comportat bine.</div>
