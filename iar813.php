<h1 style="margin: 2% 10% 0% 10%">I.A.R.-813 (1950)</h1>
<h2 style="margin: 0% 10% 0% 10%">Avion de școală, antrenamet și acrobație</h2>

<font size=4>
<div style="text-align: justify; margin: 2% 10% 0% 10%"><b>Anvergura 10,00 m, lungimea 8,35 m, înălțimea 2,25 m, suprafața portantă 15,00 m<sup>2</sup>, greutatea gol 498 kg, greutatea totală 750 kg, motorul:<i> Walter Minor</i> 105 C.P., viteza maximă 102 km/oră, viteza minimă 75 km/oră, plafonul 5.800 m, distanța maximă de zbor 700 km, lungimea rulajului la decolare 180 m, lungimea rulajului la aterizare 150 m. </b> </div>
<div style="text-align: justify; text-indent: 40pt; margin: 2% 10% 0% 10%"> Avionul I.A.R.-813  a fost conceput și construit de un colectiv al <i>Uzinelor de reparații material volant</i> (U.R.M.V.-3) din Brașov, după proiectul inginerului <b> Radu Manicatide.</b> </div>
<div style="text-align: justify; text-indent: 40pt; margin: 0% 10% 0% 10%"> Avionul I.A.R.-813 are la activul său o serie de recorduri de înălțime și viteză, naționale și internaționale, din clasa C-1b (avioane cu greutate de 500-1.000 kg la decolare).</div>
<div style="text-align: justify; text-indent: 40pt; margin: 0% 10% 0% 10%"> În ziua de 12 aprilie 1957, decolând de pe aerodromul Strejnic (Ploiești), aviatoarea <b>Elena Barac</b> a stabilit, cu un avion I.A.R.-813, recordul național de înălțime (6.461 m).</div>
<div style="text-align: justify; text-indent: 40pt; margin: 0% 10% 0% 10%"> În anul 1958, zburând în formație cu trei avioane I.A.R.-813, piloții <b>Constantin Manolache</b> , <b>Simion Oțoiu</b>  și <b>Constantin Onciu</b>  au realizat trei recorduri internaționale de viteză pe un parcurs recunoscut, omologat de <b> Federația Aeronautică Internațională</b>  (F.A.I.). În ziua de 4 iulie 1958 cele trei avioane au parcurs distanța dintre București și Kiev în 4 ore, 11 minute și 43 de secunde, zburând cu o viteză de 177,941 km/oră. La 25 iulie cei trei piloți au zburat de la Moscova la Kiev, cu o viteză de 160,796 km/oră, iar a doua zi au atins viteza de 172,354 km/oră, parcurgând, tot în formație, distanța Kiev-București în 4 ore, 19 minute și 52 de secunde.</div>
<div style="text-align: justify; text-indent: 40pt; margin: 0% 10% 0% 10%"> Tot la bordul unui avion I.A.R.-813, pilotul <b>Bănică Enciulescu</b> a stabilit într-o singură zi două recorduri naționale de viteză: pe un circuit închis de 500 km, cu o viteză de 197,954 km/oră și pe un circuit închis de 1.000 km, cu viteza de 197,335 km/oră. </div>
</font>

