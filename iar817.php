<h1 style="margin: 2% 10% 0% 10%">I.A.R.-817 (1955)</h1>
<h2 style="margin: 0% 10% 0% 10%">Avion utilitar</h2>

<font size=4>
<div style="text-align: justify; margin: 2% 10% 0% 10%"><b> Anvergura 12,60 m, lungimea 9,80 m, înălțimea 3,40 m, suprafața portantă 25,40 m<sup>2</sup>, greutatea gol 800 kg, greutatea totală 1.150 kg, motorul:<i> Walter Minor 6 III</i> 160 C.P., viteza maximă 175 km/oră, viteza minimă 70 km/oră, plafonul 3.000 m, timpul de urcare până la 1.000 m 7 minute și 30 secunde, distanța maximă de zbor 560-930 km, lungimea rulajului la decolare 150-180 m, lungimea rulajului la aterizare 60 m. </b> </div>
<div style="text-align: justify; text-indent: 40pt; margin: 2% 10% 0% 10%">Este primul avion utilitar fabricat în țara noastră. A fost conceput și realizat de un colectiv din cadrul Uzinelor de reparații material volant (U.R.M.V.-3) din Brașov, sub conducerea inginerului Radu Manicatide. Trenul de aterizare triciclu, cu bechie orientabilă în bot, avea amortizoare de cauciuc și frâne mecanice la roți. </div>
<div style="text-align: justify; text-indent: 40pt; margin: 0% 10% 0% 10%">Avionul I.A.R.-817 putea fi utilizat pentru misiuni agricole, antrenamente de parașutism, transporturi poștale, parașutare de medicamente, precum și ca avion sanitar, variantă în care a fost construit în anul 1957 sub denumirea I.A.R.-817 S (cu trei locuri-pilot,bolnav pe targă și însoțitor). </div>
<div style="text-align: justify; text-indent: 40pt; margin: 0% 10% 0% 10%"> Forma subțiată a fuzelajului spre ampenaj a permis plasarea unei uși cu deschidere spre înapoi, asigurându-se astfel îmbarcarea ușoară și rapidă a diverselor încărcături sau a tărgii (în cazul variantei sanitare). Cabina, închisă complet, avea încă două uși laterale, iar în podea era amenajată o trapă care putea fi deschisă în timpul zborului, pentru parașutarea coletelor cu medicamente sau pentru montarea unui aparat fotoaerian.</div>
</font>

