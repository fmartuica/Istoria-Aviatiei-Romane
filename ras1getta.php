<h2 style="margin: 0% 10% 0% 10%">Hidroavion de școală și antrenament</h2>
<font size=4>
<div style="text-align: justify; margin: 2% 10% 0% 10%"> 
<b>Anvergura 16,00 m, lungimea 10,50 m, înălțimea 3,50 m, suprafața portantă 50,00 m<sup>2</sup>, greutatea celulei 825 kg, greutatea grupului motopropulsor 420 kg, încărcătură utilă 800 kg, greutatea totală 2.045 kg, motorul: <i>Hiero</i> (răcire cu apă) 220 C.P., viteza maximă 160 km/oră, viteza de croazieră 140 km/oră, plafonul 4.000 m, autonomia de zbor, 8 ore.
</b></div>

<div style="text-align: justify; text-indent: 40pt; margin: 2% 10% 0% 10%">  	Prototipul acestui hidroavion și trei exemplare de serie au fost realizate de inginerul Radu Stoika, căruia îi aparținea și proiectul, în cadrul Societății de transport-Constanța (S.T.C.).</div>
<div style="text-align: justify; text-indent: 40pt; margin: 0% 10% 0% 10%">  		Primele zboruri de probă cu hidroavionul R.A.S.-1 Getta, au fost efectuate în ziua de 27 august 1925, la hidroscala instalată la bazinul „Titan” din portul Constanța, de cunoscutul pilot căpitan aviator Romeo Popescu, secondat de locotenentul Petre Diaconescu și de <i>Ion Culluri.</i></div>
<div style="text-align: justify; text-indent: 40pt; margin: 0% 10% 0% 10%"> 	Hidroavionul R.A.S.-1 Getta era secviplan (cu aripi inegale) pendular, destinat trecerii piloților de pe avioane pe hidroavioane, și avea trei locuri (pentru pilot, navigator și mecanic). </div>
<div style="text-align: justify; text-indent: 40pt; margin: 0% 10% 0% 10%"> Coca fuzelajului avea lonjeroane din lemn de frasin și era împărțită în compartimente etanșe, spre a se evita umplerea totală cu apă, în cazul deteriorării învelișului. Elicea de lemn, blindată la capetele palelor, era propulsivă.Coca fuzelajului avea lonjeroane din lemn de frasin și era împărțită în compartimente etanșe, spre a se evita umplerea totală cu apă, în cazul deteriorării învelișului. Elicea de lemn, blindată la capetele palelor, era propulsivă. </div>
<div style="text-align: justify; text-indent: 40pt; margin: 0% 10% 0% 10%">Dintre cele patru exemplare construite, prototipul a fost folosit la efectuarea probelor statice de rezistență, iar celelalte trei exemplare de serie au constituit prima formațiune de școală a Flotei de hidroaviației, instalată la hidroșcoala de la Mamaia, pe lacul Siutghiol. </div>

