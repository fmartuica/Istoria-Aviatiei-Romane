<font size=4>
<div style="text-align: justify; text-indent: 40pt; margin: 2% 10% 0% 10%"><b>Ștefan Protopopescu</b> era originar din Mehedinți. Este atras de aviație și urmează școala din cadrul Complexului Aeronautic de la Chitila (lângă București), înființat la 20 noiembrie 1909. Aici, în data de 9 iulie 1911 sublocotenentul Ștefan Protopopescu obține brevetul de pilot militar, devenind primul pilot brevetat în România și primul pilot al armatei române. Zborul de brevetare l-a făcut cu un aparat de tip Farman III model 1909, construit în licență în atelierele de la Chitila. Imediat după el, în data de 17 iulie obține în condiții similare brevetul de pilot și colegul său, sublocotenentul Gheorghe Negrescu. În anii 1913-1914 ambii vor studia la Școala Superioară de Construcții Mecanice și Aeronautice din Paris, devenind primii ingineri militari de aviație ai armatei române.  </div>
<div style="text-align: justify; text-indent: 40pt; margin: 0% 10% 0% 10%">La 1 iulie 1920 se înființează <i>Arsenalul Aeronautic</i>, la conducerea căruia este numit maiorul inginer aviator Ștefan Protopopescu. După proiectul realizat de el se construiește avionul Proto, care avea să fie în anul 1922 primul avion autohton construit într-o unitate industrială românească.  </div>
<div style="text-align: justify; text-indent: 40pt; margin: 0% 10% 0% 10%"> În anul 1923, la fabrica Astra din Arad, după un alt proiect al său se realizează 25 de exemplare ale aparatului Proto-1. În urma unui accident în timpul încercărilor, accident în care a murit pilotul de încercare al fabricii, Ion Sava, unul din cei mai buni piloți ai Primului Război Mondial, avioanele sunt consolidate, devenind Proto 2. Acestea au fost folosite ca avioane de școală pentru faza a II-a, ce corespundea obținerii brevetului de zbor internațional. </div>
<div style="text-align: justify; text-indent: 40pt; margin: 0% 10% 0% 10%"> În anul 1924 se fondează la București <i>Societatea pentru exploatări tehnice</i> (S.E.T.), unde după planurile lui Ștefan Protopopescu se execută avionul Proto-3. Tot aici se realizează două exemplare din biplanul de recunoaștere Proto-S.E.T., după un proiect al său în colaborare cu inginerul Dumitru Baziliu. </div>
<div style="text-align: justify; text-indent: 40pt; margin: 0% 10% 0% 10%"> În 1924, la cererea Guvernului României întocmește un memoriu privind înființarea unei fabrici de avioane și motoare, memoriu susținut de șeful de Stat Major, colonelul Paul Teodorescu. În urma acestei acțiuni, Parlamentul României adoptă în anul 1925 „Legea privitoare la întreprinderile industriale în legătură cu apărarea națională”, actul de naștere al fabricii de avioane care avea să poarte denumirea de <i>Industria Aeronautică Română</i> (IAR).  </div>
