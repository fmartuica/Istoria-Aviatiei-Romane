<h1 style="margin: 2% 10% 0% 10%">I.A.R.-80 (1939)</h1>
<h2 style="margin: 0% 10% 0% 10%">Avion de vânătoare</h2>

<font size=4>
<div style="text-align: justify; margin: 2% 10% 0% 10%"><b> Anvergura 10,50 m, lungimea 8,90 m, înălțimea 3,60 m, suprafața portantă 16,00 m<sup>2</sup>, greutatea gol 1.780 kg, greutatea totală 2.550 kg, motorul: <i>I.A.R. K 14-1000 A</i> 1.000 C.P., viteza maximă (la 4.500 m) 510 km/oră, plafonul 10.500 m, timpul de urcare până la 1.000 m 1 minut și 20 secunde, timpul de urcare până la 4.500 m 5 minute și 40 de secunde, distanța maximă de zbor 940 km.</b> </div>
<div style="text-align: justify; text-indent: 40pt; margin: 2% 10% 0% 10%"> Avionul I.A.R-80 a trecut cu succes probele de zbor în anul 1.939, fiind pilotat de căpitanul aviator <b>Dumitru (Pufi) Popescu</b>. Era un monoplan (cu planul jos), monoloc, de construcție în întregime metalică. Partea anterioară a fuzelajului avea scheletul din tuburi de oțel sudate, de tip grindă cu zăbrele, iar partea posterioară avea scheletul din duraluminiu, format din patru lonjeroane, cu două cadre principale, în formă de „dublu T” și cu cinci cadre intermediare, cu secțiunea în formă de Z. </div>
<div style="text-align: justify; text-indent: 40pt; margin: 0% 10% 0% 10%"> Suportul motorului era format dintr-o coroană metalică, cu elemente de rezistență, ce se lega în patru puncte la fuzelaj.</div>
<div style="text-align: justify; text-indent: 40pt; margin: 0% 10% 0% 10%">Structura de rezistență a aripii era constituită din două lonjeroane și 38 de nervuri din duraluminiu. Învelișul era în întregime din tablă de duraluminiu; numai eleroanele (cu schelet din tuburi de duraluminiu) aveau înveliș de pânză. Aceeași structură metalică o avea și ampenajul, stabilizatorul și deriva, care erau învelite cu tablă de duraluminiu, iar profundorul și direcția cu pânză. Pe ambele planuri, între eleroane și fuzelaj, erau dispuse flapsurile (cu comandă hidraulică). </div>
<div style="text-align: justify; text-indent: 40pt; margin: 0% 10% 0% 10%"> Trenul de aterizare era escamotabil. Ecartamentul (distanța dintre roți) mare asigura o bună stabilitate în timpul rulajului. Roțile erau prevăzute cu frâne, iar bechia (fixă) avea patină metalică și un amorizor hidropneumatic tip T.U.-I.A.R.</div>
<div style="text-align: justify; text-indent: 40pt; margin: 0% 10% 0% 10%"> Carlinga avea o cupolă de plexiglas, care se deschidea prin glisare înapoi. Între carlingă și motor erau amplasate rezervorul de combustibil și un panou metalic parafoc, care proteja pilotul în caz de incendiu. Avionul I.A.R.-80 era echipat cu un amortizor de incendiu și cu două extinctoare, unul automat și altul comandat de pilot. Avionul dispunea și de o instalație de oxigen pentru zborurile la înălțimi mari.</div>
<div style="text-align: justify; text-indent: 40pt; margin: 0% 10% 0% 10%"> În afara avionului I.A.R.-80, a fost construită o variantă a acestuia (având anvergura cu 20 cm mai mare), care a căpătat denumirea de I.A.R.-81. Acest ultim tip de avion a fost construit pentru bombardamentul în picaj (I.A.R.-81 și I.A.R.-81 C) și pentru vânătoarea cu rază de acțiune mai mare (I.A.R.-81 B).</div>
<div style="text-align: justify; text-indent: 40pt; margin: 0% 10% 0% 10%">Armamentul avioanelor I.A.R.-80 și I.A.R.-81, în diverse variante, este prezent în tabelul de mai jos: </div>
<center>
<img src="poze/iar80-1.png" alt="avion" width="60%">
</center>
<div style="text-align: justify; text-indent: 40pt; margin: 0% 10% 0% 10%"> Variantele de bombardament în picaj ale avionului I.A.R.-81 aveau lansatoare de care se puteau acroșa două bombe de câte 50 de kg și o bombă de 250 kg.</div>
<div style="text-align: justify; text-indent: 40pt; margin: 0% 10% 0% 10%">Varianta I.A.R.-81 B avea acroșate sub planuri (în locul bombelor) două rezervoare suplimentare. </div>
<div style="text-align: justify; text-indent: 40pt; margin: 0% 10% 0% 10%"> Având motor puternic și fiind foarte maniabil, deci dispunând de o mare capacitate de manevră, avionul I.A.R.-80, cu performanțele sale remarcabile, poate fi apreciat ca cel mai reușit dintre avioanele proiectate și construite la fabrica I.A.R. din Brașov.</div>
<div style="text-align: justify; text-indent: 40pt; margin: 0% 10% 0% 10%"> La data apariției sale, avionul I.A.R.-80 se situa printre cele mai bune avioane de vânătoare din lume. Întrucât viteza constituie performanța principală a unui avion de vânătoare, prezentăm mai jos un tabel comparativ cu vitezele unor avioane de vânătoare, aflate în dotarea aviației diferitelor țări, în anul 1939:</div>
<center>
<img src="poze/iar80-2.png" alt="avion" width="60%">
</center>
<div style="text-align: justify; text-indent: 40pt; margin: 0% 10% 0% 10%"> Reiese că din aceste 20 de tipuri de avioane de vânătoare, aflate în dotarea aviației militare a unor țări avansate în domeniul construcțiilor aeronautice, numai trei avioane depășeau viteza avionului I.A.R.-80, care a rămas în serviciul aviației noastre de vânătoare până la sfârșitul celui de-Al Doilea Război Mondial.</div>
<div style="text-align: justify; text-indent: 40pt; margin: 0% 10% 0% 10%">În anul 1950 s-a realizat, în cadrul <i>Atelierelor de reparații material volant</i> (A.R.M.V) de la Pipera, o variantă biloc a avionului I.A.R.-80, denumită I.A.R.-80 D.C. (cu dublă comandă), destinată pentru trecerea piloților militari pe avioane de vânătoare. Această variantă s-a dovedit a fi reușită, întrucât, cu toate modificările aduse, avionul și-a menținut calitățile de zbor. I.A.R.-80 D.C. a rămas în dotarea școlilor militare de pilotaj până în anul 1952. </div>
</font>

