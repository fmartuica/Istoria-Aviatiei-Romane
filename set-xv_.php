<h2 style="margin: 0% 10% 0% 10%">Hunting plane</h2>

<font size=4>
<div style="text-align: justify; text-indent: 40pt; margin: 2% 10% 0% 10%">S.E.T.- XV plane had a metalic structure and had, among other special onboard facilities, an oxygen plant for flying at high heights and an on-board radio station (broadcast and reception). The wing had an original S.E.T. profile and it was provided with flaps that seemed to be the force of sustainability. Due to its aerodynamic qualities, S.E.T. plane had a good stability. The weapons consisted of two machine guns, firing through the propelles field, located in the engine hood. </div>
