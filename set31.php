<h1 style="margin: 2% 10% 0% 10%">S.E.T.-31 (1930)</h1>
<h2 style="margin: 0% 10% 0% 10%">Avion de școală, antrenament și legătură</h2>

<font size=4>
<div style="text-align: justify; margin: 2% 10% 0% 10%"><b> Anvergura planului superior 9,80 m, anvergura planului inferior 9,50 m, lungimea 7,20 m, înălțimea 3,15 m, suprafață portantă 26,60 m<sup>2</sup>, greutatea gol 826 kg, greutate totală 1.120 kg, motorul <i>Salmson 9 Ab</i> 230 C.P., viteza maximă 215 km/oră, viteza la 2.000 m, 198 km/oră, viteza minimă 80 km/oră, plafonul 6.000 m, timpul de urcare până la 2.000 m, 6 minute 20 secunde, autonomia de zbor 2 ore 54 minute, lungimea rulajului la decolare 85 m, lungimea rulajului la aterizare 160 m.</b> </div>
<div style="text-align: justify; text-indent: 40pt; margin: 2% 10% 0% 10%"> Avionul S.E.T.-31 avea scheletul de lemn și învelișul de pânză, fiind destinat pentru pregătirea piloților în vederea trecerii de pe avioane de școală pe cele de luptă. Cu acest avion se putea executa întreaga gamă de figuri acrobatice.</div>
<div style="text-align: justify; text-indent: 40pt; margin: 0% 10% 0% 10%">Avionul S.E.T.-31 a fost construit în serie și are la activul său câteva zboruri mai deosebite. În anul 1931, cu un avion S.E.T.-31 s-a executat un raid în Polonia, acoperindu-se distanța de 2.300 km în 11 ore de zbor, iar în anul 1933, un alt raid de 18.000 km pe o traiectorie care a trecut prin 13 orașe din Europa. </div>
<div style="text-align: justify; text-indent: 40pt; margin: 0% 10% 0% 10%">În perioada 1-4 mai 1931, trei echipaje românești au participat, cu avionul S.E.T.-31, la un concurs aeronautic internațional, desfășurat la Plzen (Cehoslovacia), alături de nouă echipaje cehoslovace, trei germane și unul elvețian. Piloții români s-au prezentat la toate cele trei probe ale concuruslui - regularitate, viteză și acrobație - , ocupând locul I în clasamentul pe echipe și locul I și III în clasamentul general individual. </div>
<div style="text-align: justify; text-indent: 40pt; margin: 0% 10% 0% 10%"> În anul 1935, o formație de șapte avioane S.E.T.-31 din cadrul aviației militare, sub comanda căpitan-comandorului inginer aviator <b>Gheorghe Negrescu</b>, a executat un raid în formație pe traiectul: București-Istambul-Eskisheir-Ankara și retur.</div>
</font>

