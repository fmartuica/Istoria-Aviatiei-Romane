<font size=4>
<div style="text-align: justify; text-indent: 40pt; margin: 2% 10% 0% 10%"> Constructor de avioane şi motoare, inventatorul român <b>Traian Vuia</b> a fost un pionier al aviaţiei mondiale. S-a născut în 1872, într-un sat din judeţul Timiş. După terminarea liceului, pleacă la Budapesta, unde a urmat o vreme cursurile Şcolii Politehnice.  </div>
<div style="text-align: justify; text-indent: 40pt; margin: 0% 10% 0% 10%"> Marea lui pasiune a fost tehnica. Pe 1 iulie 1902 sosea la Paris, aducând cu el schiţa unui original “aeroplan-automobil”, conceput în perioada studenţiei, şi macheta aferentă realizată pe parcursul ultimelor 12 luni. Deoarece Parisul era considerat la vremea aceea centrul mondial al aeronauticii, tânărul Vuia spera că acolo va reuşi să-şi îndeplinească visul de a zbura. El depune la Academia de Științe a Franţei un memoriu intitulat “proiect de aeroplan-automobil”, în care își prezenta invenţia. Deşi a primit un aviz nefavorabil, primeşte totuşi un brevet de invenţie francez în 1903, începând chiar din toamna acelui an construcţia aeroplanului-automobil. Lucrările s-au încheiat în 1905, în luna decembrie, dar din cauza condiţiilor meteorologice nefavorabile ziua primei testări a fost 18 martie 1906, o zi rămasă în istoria aeronauticii ca simbol al începutului unei noi ere, în care aparatele mai grele decât aerul se puteau ridica de la sol şi puteau zbura prin mijloace proprii.  </div>
<div style="text-align: justify; text-indent: 40pt; margin: 0% 10% 0% 10%">  Aparatul, denumit Vuia I, a fost poreclit “liliacul”, datorită formei sale. Era un monoplan uşor, cu cadru din ţevi de oţel, aripi pliabile de pânză întinse pe un schelet metalic, o singură elice, tren de aterizare format din patru roţi dotate cu pneuri şi motor ce funcţiona cu anhidridă carbonică. Era o premieră folosirea pneurilor la trenul de aterizare, iar motorul era de concepţie proprie. Vuia I se găseşte expus acum în Muzeul Aerului din Paris. </div>
<div style="text-align: justify; text-indent: 40pt; margin: 0% 10% 0% 10%">   	Primul zbor integral mecanic s-a efectuat pe o distanţă de aproximativ 12 metri, la o înălţime de aproape un metru deasupra solului. Apoi a apărut “Vuia I bis”, modificat, cu care inventatorul a reuşit să se desprindă ceva mai mult de sol. După Vuia I a urmat Vuia II, echipat cu un motor de 25 CP (cai putere) cu 9 cilindri în V, cu care a parcurs în zbor distanţa de 70 de m.</div>
