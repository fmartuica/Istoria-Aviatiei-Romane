<div style="text-align: justify; text-indent: 40pt; margin: 0% 10% 0% 10%">  A urmat o pauză impusă de lipsa finanţărilor şi de izbucnirea războiului. A reuşit să îşi reia activitatea abia în 1918, construind în numai patru ani două tipuri originale de elicopter, prevăzute cu rotor portant cu ax înclinat pe direcţia de zbor, o adevărată inovaţie pentru acele vremuri. Şi-a testat invenţiile pe aerodromurile Juvissy şi Moulineaux, în Franţa. </div>
<div style="text-align: justify; text-indent: 40pt; margin: 0% 10% 0% 10%"> 	Numele lui Traian Vuia mai este legat şi de o altă mare invenţie din 1925: generatorul cu aburi, ce avea randament termic ridicat. Invenţia a fost brevetată în mai multe ţări, nu doar în Franţa.  </div>
<div style="text-align: justify; text-indent: 40pt; margin: 0% 10% 0% 10%">  În ziua de 27 mai 1946 devine membru de onoare al Academiei Române. </div>
<div style="text-align: justify; text-indent: 40pt; margin: 0% 10% 0% 10%"> S-a stins din viaţă pe 3 septembrie 1950, la Bucureşti, lăsându-şi numele înscris pe lista personalităţilor care au revoluţionat societatea modernă.  </div>
