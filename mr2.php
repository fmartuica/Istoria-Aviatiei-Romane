<h1 style="margin: 2% 10% 0% 10%">M.R.-2 (1956)</h1>
<h2 style="margin: 0% 10% 0% 10%">Avion de transport ușor</h2>

<font size=4>
<div style="text-align: justify; margin: 2% 10% 0% 10%"><b>Anvergura 14,00 m, lungimea 10,90 m, înălțimea 2,76 m, suprafața portantă 28,00 m<sup>2</sup>, greutatea gol 1.415 kg, greutatea totală 2.080 kg, motoarele: două <i>Walter Minor 6 III </i>de câte 160 C.P., viteza maximă 275 km/oră, viteza minimă 85 km/oră, plafonul 4.900 m, distanța maximă de zbor 1.100 km, lungimea rulajului la decolare 400 m, lungimea rulajului la aterizare 280 m. </b> </div>
<div style="text-align: justify; text-indent: 40pt; margin: 2% 10% 0% 10%"> Proiectul avionului M.R.-2 aparține inginerului <b>Radu Manicatide</b>. Avionul a fost realizat în cadrul <i>Uzinelor de reparații material volant </i>(U.R.M.V.-3) din Brașov.</div>
<div style="text-align: justify; text-indent: 40pt; margin: 0% 10% 0% 10%"> Avionul M.R.-2  este o variantă a aparatului I.A.R.-814. Poate transporta, în afară de pilot, cinci pasageri sau, în variantă sanitară, doi bolnavi pe targă și un însoțitor. Aripile, cu un singur lonjeron de lemn, au înveliș de placaj. Planul central era metalic. Fuzelajul, cu scheletul din tuburi de oțel sudate, este acoperit parțial cu tablă, parțial cu pânză. Ampenajul orizontal este din lemn, iar cel vertical are schelet metalic, acoperit cu pânză.</div>
<div style="text-align: justify; text-indent: 40pt; margin: 0% 10% 0% 10%">Trenul de aterizare, escamotabil prin comandă electrică, are amortizoare de cauciuc și frâne mecanice. Elicele, metalice, au pasul variabil (comandat electric). </div>
</font>

