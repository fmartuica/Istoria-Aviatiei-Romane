<h1 style="margin: 2% 10% 0% 10%">AERON (1934)</h1>
<h2 style="margin: 0% 10% 0% 10%">Avion de școală și antrenament</h2>

<font size=4>
<div style="text-align: justify; margin: 2% 10% 0% 10%"><b> Anvergura 9,80 m, lungimea 6,45 m, înălțimea 2,45 m, greutatea gol 760 kg, greutatea totală 1.010 kg, motorul : <i>Salmson 9 Ac</i> 120 C.P., viteza maximă 187 km/oră, viteza minimă 65 km/oră, plafonul 4.200 m. </b> </div>
<div style="text-align: justify; text-indent: 40pt; margin: 2% 10% 0% 10%"> Avionul Aeron a fost construit în cadrul <b>Arsenalului Aeronautic</b> din București, după proiectele unui colectiv compus din: căpitanul inginer <b>Constantin Istrate</b>, căpitanul inginer <b>Cristea Constantinescu</b>, maiorul aviator <b>Petre Macavei</b> și locotenentul inginer <b>Simion Stănculescu</b>. Au fost realizate două prototipuri , ambele încercate în zbor. Primul dintre ele, echipat cu un motor Cirrus de 105 C.P., era biplan cu aripi inegale, cu un carenaj original la trenul de aterizare. Al doilea prototip, ale cărui caracteristici și performanțe sunt prezentate mai sus, era tot un biplan, care se remarca prin formula de amplasare a planului inferior decalat față de cel superior, fără montații sau hobane între ele.</div>
<div style="text-align: justify; text-indent: 40pt; margin: 0% 10% 0% 10%">Avionul Aeron a fost pilotat și de aviatorul francez <b>Lepreux</b>, care s-a arătat foarte mulțumit de comportamentul avionului. </div>
<div style="text-align: justify; text-indent: 40pt; margin: 0% 10% 0% 10%">Cităm din presa vremii un extras dintr-o notă, apărută sub titlul <b>„Un nou avion românesc”</b>: </div>
<div style="text-align: justify; text-indent: 40pt; margin: 2% 10% 0% 10%"><i> <b>„...după avionul S.E.T. al inginerului Zamfirescu, noul avion Aeron vine ca o confirmare indiscutabilă a calităților de constructori ale inginerilor noștrii din aeronautică. Statul trebuie să-i încurajeze mai departe, spre binele industriei noastre naționale.”</b> </i></div>
<div style="text-align: justify; text-indent: 40pt; margin: 2% 10% 0% 10%"> Cu toate calitățile sale promițătoare, avionul Aeron nu a fost construit în serie.</div>
</font>

