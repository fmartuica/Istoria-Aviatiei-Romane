<font size=4>
<div style="text-align: justify; margin: 2% 10% 0% 10%"><b>Anvergura 12,80 m, lungimea 9,20 m, înălțimea 2,80 m, suprafața portantă 26,00 m<sup>2</sup>, greutatea gol 1.130 kg, greutatea totală 1.500 kg, motorul:<i> A.L.-14 R.F.</i> 300 C.P., viteza maximă 220 km/oră, viteza de croazieră 185 km/oră, viteza minimă 60 km/oră, viteza verticală de urcare 7 m/s, timpul de urcare până la 1.000 m 3 minute, plafonul 6.500 m, distanța maximă de zbor 650 km, autonomia de zbor 3 ore și 30 minute. </b> </div>
