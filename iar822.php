<h1 style="margin: 2% 10% 0% 10%">I.A.R.-822 (1970)<h1>
<h2 style="margin: 0% 10% 0% 10%">Avion pentru misiuni agricole</h2>

<font size=4>
<div style="text-align: justify; margin: 2% 10% 0% 10%"><b>Anvergura 12,80 m, lungimea 9,40 m, înălțimea 2,80 m, suprafața portantă 26,00 m<sup>2</sup>, greutatea gol (echipat) 1.120 kg, greutatea totală 1.900 kg, motorul: <i>Lycoming IO-540 G1 D5</i> 290 C.P., viteza maximă (admisă) 230 km/oră, viteza de lucru 120-160 km/oră, viteza minimă 65 km/oră, plafonul 4.500 m, distanța maximă de zbor 350 km, autonomia de zbor 2  ore și 20 de minute. </b> </div>
<div style="text-align: justify; text-indent: 40pt; margin: 2% 10% 0% 10%"> Avionul I.A.R.-822 a fost construit la <b>I.R.M.A.</b>-București, după un proiect elaborat în cadrul <i>Institutului de mecanica fluidelor și construcții aerospațiale</i> de un colectiv sub conducerea inginerului <b>Radu Manicatide</b>, fiind primul avion românesc conceput special pentru misiuni agricole. Este un monoplan de construcție mixtă, cu aripa jos. Scheletul fuzelajului este din tuburi de oțel crom-molibden sudate. Partea anterioară a fuzelajului este acoperită cu panouri de duraluminiu, iar cea posterioară cu pânză. În fuzelaj, între carlingă și panoul parafoc, este instalat un rezervor pentru substanțe chimice (lichide sau solide), cu o capacitate de 800 litri.</div>
<div style="text-align: justify; text-indent: 40pt; margin: 0% 10% 0% 10%"> Aripile, cu scheletul format din lonjeroane și nervuri de lemn, sunt acoperite cu placaj și pânză (peste placaj).</div>
<div style="text-align: justify; text-indent: 40pt; margin: 0% 10% 0% 10%"> Scheletul ampenajului este de lemn, deriva și stabilizatorul fiind acoperite cu placaj, iar direcția și profundorul cu pânză.</div>
<div style="text-align: justify; text-indent: 40pt; margin: 0% 10% 0% 10%">Ca avion agricol, I.A.R.-822 poate fi folosit pentru: </div>
<div style="text-align: justify; text-indent: 40pt; margin: 0% 10% 0% 10%"> - fertilizare, prin răspândirea de îngrășăminte chimice, solide sau lichide;</div>
<div style="text-align: justify; text-indent: 40pt; margin: 0% 10% 0% 10%">- protecția culturilor, prin prăfuirea sau stropirea cu substanțe chimice; </div>
<div style="text-align: justify; text-indent: 40pt; margin: 0% 10% 0% 10%"> - însămânțare;</div>
<div style="text-align: justify; text-indent: 40pt; margin: 0% 10% 0% 10%">- supravegherea terenurilor agricole sau a zonelor împădurite. </div>
</font>

