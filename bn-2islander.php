<h1 style="margin: 2% 10% 0% 10%">B.N.-2 ISLANDER (1969)</h1>
<h2 style="margin: 0% 10% 0% 10%">Avio de transport ușor</h2>

<font size=4>
<div style="text-align: justify; margin: 2% 10% 0% 10%"><b>Anvergura 14,92 m, lungimea 10, 90m, înălțimea 4,16 m, greutatea gol 1.590 kg, greutatea totală 2.590 kg, motoarele: două <i>Lycoming 0-540-E</i> de câte 264 C.P., viteza maximă 270 km/oră, viteza verticală de urcare: - cu ambele motoare 6,2 m/s, - cu un motor 1,25 m/s, plafonul 6.100 m, distanța maximă de zbor 1.130 km, lungimea rulajului la decolare 265 m, lungimea rulajului la aterizare 128 m. </b> </div>
<div style="text-align: justify; text-indent: 40pt; margin: 2% 10% 0% 10%">Este un avion destinat zborurilor comerciale pe distanțe scurte, construit la <i>I.R.M.A.</i>-București, după documentația firmei engleze <b>Britten Norman</b>. Scheletul avionului este metalic, acoperit în întregime cu aluminiu anticorosiv. Datorită faptului că distanța dintre fuzelaj și sol este mică, urcarea pasagerilor în cabină se face direct, fără ajutorul unei scări. </div>
<div style="text-align: justify; text-indent: 40pt; margin: 0% 10% 0% 10%"> Echipajul avionului B.N.-2 este format dintr-un singur pilot. Postul de pilot poate fi însă amenajat și pentru dublă comandă, în scopul antrenamentului sau al unor zboruri de durată, fiind echipat cu instalații moderne de navigație și control pentru zborul în condiții meteorologice grele. De asemenea, avionul B.N.-2 poate fi amenajat și pentru alte misiuni, ca de exemplu: transport de mărfuri, avion sanitar, parașutare de colete în locuri greu accesibile, sau ca un avion de lux pentru curse speciale, precum și pentru antrenamentul parașutiștilor.</div>
