<h1 style="margin: 2% 10% 0% 10%">Proto-1 (1922)</h1>
<h2 style="margin: 0% 10% 0% 10%">Avion de școală și perfecționare</h2>

<font size=4>
<div style="text-align: justify; margin: 2% 10% 0% 10%"><b>Anvergura 9,60 m, lungimea 7,00 m, înălțimea 2,90 m, suprafața portantă 28,86 m<sup>2</sup>, greutate gol 670 kg, greutatea totală 1080 kg, motorul: <i>Hispano-Suiza</i> 180 C.P., viteza maximă 183 km/oră, viteza minimă 81 km/oră, plafonul 6000 m, timpul de urcare până la 4000 m- 29 minute. </b> </div>
<div style="text-align: justify; text-indent: 40pt; margin: 2% 10% 0% 10%">Construit la <i>Arsenalul Aeronautic</i> din București, după proiectele elaborate de maiorul inginer aviator Ștefan Protopopecu, avionul Proto-1 a marcat prin apariția sa, începutul fabricației aeronautice în țara noastră, fiind primul avion românesc realizat în cadrul unei întreprinderi specializate. Din colectivul care a lucrat sub conducerea maiorului inginer Ștefan Protopopescu la definitivarea proiectului acestui avion, o contribuție de seamă au adus-o inginerii Dumitru Baziliu și Gheorghe Țicău. </div>
<div style="text-align: justify; text-indent: 40pt; margin: 0% 10% 0% 10%">Primele zboruri de încercare cu exemplarul-prototip au fost executate chiar de constructorul avionului, maiorul inginer aviator Ștefan Protopopescu, posesorul brevetului de pilot nr. 1 (din România). </div>
<div style="text-align: justify; text-indent: 40pt; margin: 0% 10% 0% 10%"> 	Ministrul de război a comandat construirea unei serii de 25 de exemplare de acest tip. Nerespectând proiectul prototipului, executat la Arsenalul Aeronautic din București, fabrica Astra a modificat din proprie inițiativă scheletul planurilor, fapt care a dus la micșorarea rezistenței acestora. Din această cauză, în timpul unuia din primele zboruri de probă, executat cu avionul cap de serie, în anul 1923, una din aripi a cedat și avionul s-a angajat, prăbușindu-se în râul Mureș. În acest accident și-a pierdut viața unul dintre cei mai buni piloți de încercare, locotenentul aviator Ion Sava.</div>
<div style="text-align: justify; text-indent: 40pt; margin: 0% 10% 0% 10%"> Sistându-se construcția în serie, s-a impus refacerea calculelor de rezistență, după care, la aceeași fabrică, a fost realizat un nou avioan care a căpătat denumirea de <b>Proto-2</b>.</div>
</font>

