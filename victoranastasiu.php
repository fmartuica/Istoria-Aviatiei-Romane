<h2 style="margin: 2% 10% 0% 10%">Victor Anastasiu: un medic genial, devotat meseriei de pilot!</h2>

<font size=4>
<div style="text-align: justify; text-indent: 40pt; margin: 2% 10% 0% 10%"> În martie 1916 a fost brevetat pilot locotenentul dr. Victor Anastasiu, primul medic român (şi al doilea din lume) care a obţinut acest titlu. </div>
<div style="text-align: justify; text-indent: 40pt; margin: 2% 10% 0% 10%">În această calitate a efectuat mai multe studii asupra comportării organismului uman în timpul zborului şi a participat, totodată, la lupte aeriene în Primul Război Mondial.  </div>
<div style="text-align: justify; text-indent: 40pt; margin: 0% 10% 0% 10%">În 1920 a pus bazele Centrului de Medicină Aeronautică (Serviciului Sanitar Aeronautic şi al Centrului Medical Aeronautic), printre primele astfel de instituţii din lume, cercetările sale bazate inclusiv pe propria experienţă în zbor fiind determinante pentru sănătatea aviatorilor. </div>
<div style="text-align: justify; text-indent: 40pt; margin: 0% 10% 0% 10%">Studierea comportamentului piloţilor în timpul zborurilor a fost o premieră mondială, aspect recunoscut în cadrul Congresului Internaţional de navigaţie aeriană de la Paris din 1921 de către Charles Robert Richet. </div>
<div style="text-align: justify; text-indent: 40pt; margin: 0% 10% 0% 10%">În prezent, Institutul Naţional de Medicină aeronautică poartă numele medicului român, Institutul Naţional de Medicină aeronautică şi spaţială "General doctor aviator Victor Anastasiu". </div>
