<h1 style="margin: 2% 10% 0% 10%">R.G.-5 PESCĂRUȘ (1957)</h1>
<h2 style="margin: 0% 10% 0% 10%">Performance Glider</h2>

<font size=4>
<div style="text-align: justify; text-indent: 40pt; margin: 2% 10% 0% 10%"> The good aerodynamic Qualities of the board R.G.-5 and, in particular, the sensitivity to the controls and the vertical speed of low descent allowed him to be easily maintained on the slope flight and weak thermic currents.</div>
<div style="text-align: justify; text-indent: 40pt; margin: 0% 10% 0% 10%">In the year 1958, Aurelia Roşianu-Gheorghiu established with a glider R.G.-5 The national female speed record on a 100 km path, with a fixed goal, for class D1 gliders, with a performance of 51.200 km/h. </div>
<div style="text-align: justify; text-indent: 40pt; margin: 0% 10% 0% 10%">The glider R.G.-5 was built at the Timber Industrialization Forestry Enterprise (I.F.I. L) in Reghin, following the project of Vladimir Noviţchi. </div>
