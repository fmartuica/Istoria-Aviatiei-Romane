<h1 style="margin: 2% 10% 0% 10%">I.S.-3 (1953)</h1>
<h2 style="margin: 0% 10% 0% 10%">Planor de performanță</h2>

<font size=4>
<div style="text-align: justify; margin: 2% 10% 0% 10%"><b> Anvergura 16 m, lungimea 6,45 m, înălțimea 1,45 m, suprafața portantă 16,00 m<sup>2</sup>, greutatea gol 215 kg, greutatea totală 305 kg, încărcarea 19,10 kg/m<sup>2</sup>, finețea maximă la viteza de 80 km/oră 30,00, viteza maximă admisă la remorcaj: automosor 90 km/oră; avion 120 km/oră, viteza maximă admisă în picaj (cu frânele scoase) 200 km/oră, viteza minimă 50 km/oră, viteza minimă de coborâre 0,65 m/s. </b></div>
<div style="text-align: justify; text-indent: 40pt; margin: 2% 10% 0% 10%"> Planorul I.S.-3, a cărui construcție îl făcea să se deosebească de planoarele clasice, a fost construit în cadrul <i>Uzinelor de reparații material volant</i> (U.R.M.V.-3) Brașov, după proiectul inginerului Iosif Șilimon. </div>
<div style="text-align: justify; text-indent: 40pt; margin: 0% 10% 0% 10%"> Fuzelajul planorului se compunea din două părți:</div>
<div style="text-align: justify; text-indent: 65pt; margin: 0% 10% 0% 10%"> a) o cocă în partea anterioară, construită din lemn și învelită cu placaj, care cuprindea postul de pilotaj, dispozitivele aterizorului și cele de lansare;</div>
<div style="text-align: justify; text-indent: 65pt; margin: 0% 10% 0% 10%"> b) un suport tubular pentru ampenaj (din tablă de duraluminiu cu grosimea de 1,5 mm), fixat de cocă în două puncte.</div>
<div style="text-align: justify; text-indent: 40pt; margin: 0% 10% 0% 10%"> Calitățile planorului au fost confirmate prin performanțe deosebite. În anul 1959 la Lesno (Polonia), inginerul Mircea Finescu, maestru emerit al sportului, a stabilit, cu un planor I.S.-3, trei recorduri naționale: de viteză pe un traiect de 300 km/oră, cu țel fix, parcurgând 305 km; de viteză pe un circuit triunghiular de 100 km, cu performanța de 72,3 km (la această probă s-a clasat și pe locul I). </div>
<div style="text-align: justify; text-indent: 40pt; margin: 0% 10% 0% 10%"> În afară de aceste rezultate, cu planorul I.S.-3 au fost stabilite și alte recorduri naționale pentru planoarele din clasa D-1: recordul de viteză pe 100 km, cu țel fix, stabilit de maestrul sportului <i>Ovidiu Popa</i>, cu timpul de 72,3 km/oră, și recordul național feminin de distanță în linie dreaptă, realizat în 1963 de <i>Aurelia Roșianu-Gheorghiu</i>, într-un zbor de 325,6 km, între Iași și comuna Cuz Vodă.</div>

