	<h2 style="text-indent: 80pt">Înființarea aviației române de vânătoare</h2>
<div style="text-align: justify; text-indent: 40pt; margin: 5% 8% 0% 8%">Când a început Primul Război Mondial, nu exista niciun avion de vânătoare. Piloții din Europa – în număr de câteva sute – se cunoșteau între ei și nu își imaginau că vor deschide focul unul împotriva celuilalt. De multe ori, în primele săptămâni de război, când se întâlneau deasupra frontului, se apropiau și se salutau militărește. Apoi, fiecare își vedea de misiunea încredințată.</div>
<div style="text-align: justify; text-indent: 40pt; margin: 0% 8% 0% 8%">Cel care a schimbat această stare de lucruri a fost Roland Garros, unul dintre cei mai faimoși aviatori francezi ai epocii. Când a început războiul se afla la Berlin. Germanii au dorit să-l aresteze dar, cumva, a reușit să ajungă la avion și să decoleze spre vest. Revenit în Franța, avea un singur gând: să doboare aviatori germani. La o primă misiune, s-a apropiat de un avion inamic, a scos revolverul și a tras toate cele șase gloanțe. Pilotul german nu a schițat niciun gest, atât de mare i-a fost surpriza. Datorită trepidațiilor avionului lui Garros, francezul nu și-a nimerit ținta.</div>
<div style="text-align: justify; text-indent: 40pt; margin: 0% 8% 0% 8%">Revenit la sol, și-a dat seama că singura posibilitate de a doborî un adversar era aceea de a avea o mitralieră în fața postului de pilotaj și de a se îndrepta cu aparatul direct spre țintă. Numai că, la primele experimente făcute într-un atelier, gloanțele – așa cum era de așteptat – au făcut praf elicea, ceea ce, în timpul zborului era curată sinucidere (aviatorii nu purtau încă parașute). I-a venit ideea să blindeze cu plăcuțe de oțel acele părți ale palelor aflate la nivel cu țeava mitralierei. Testele au durat până în primăvara anului 1915 și au arătat că gloanțele, dacă loveau elicea, ricoșau. Dar cele mai multe treceau prin câmpul elicii.</div>
<div style="text-align: justify; text-indent: 40pt; margin: 0% 8% 0% 8%">Cu această improvizație instalată pe un aparat Morane-Saulnier Type L, Garros a doborât un prim avion german la 1 aprilie 1915, deschizând era aviației de vânătoare. În săptâmânile următoare, a mai doborât încă trei avioane inamice, pentru ca, în ziua de 18 aprilie, avionul să-i fie lovit de focul tras de la sol. Roland Garros nu a murit. A aterizat forțat în teritoriul inamic și a devenit prizonier.</div>
<div style="text-align: justify; text-indent: 40pt; margin: 0% 8% 0% 8%">Au existat două consecințe. Prima: pentru că, în mod greșit, s-a crezut că Garros a doborât cinci avioane inamice, a fost instituit titlul de as al aerului pentru toți aviatorii care îi vor egala performanța. Și azi, regula este valabilă; oricine doboară cinci aparate inamice, devine as al aerului.</div>
<div style="text-align: justify; text-indent: 40pt; margin: 0% 8% 0% 8%">A doua consecință a fost inventarea avionului de vânătoare propriu-zis de către germani. Inginerul olandez Anthony Fokker, aflat în serviciul germanilor, a analizat aparatul lui Garros și, inspirat de ideea de a trage prin câmpul elicei, a inventat un mecanism care bloca trăgaciul în acele fracțiuni de secundă în care pala se afla în dreptul țevii.</div>
<div style="text-align: justify; text-indent: 40pt; margin: 0% 8% 0% 8%">În a doua jumătate a anului 1915 avioanele Fokker au dominat cerul. Câteva sute de avioane ale Antantei au fost doborâte. Căutând cu disperare o soluție, francezii au venit cu ideea de a instala o mitralieră pe planul superior al avionului Nieuport XI. La deschiderea focului, gloanțele treceau pe deasupra elicei spre țintă. Nu era chiar cea mai eficientă metodă de a lupta aerian, dar un pilot cu experiență putea obține rezultate remarcabile. Treptat, în 1916, balanța pierderilor a început să se echilibreze.</div>
<div style="text-align: justify; text-indent: 40pt; margin: 0% 8% 0% 8%">Desigur, românii, care se pregăteau să intre în război, au aflat de apariția avioanelor de vânătoare, iar francezii le-au promis o escadrilă de Nieuport XI. Avioanele urmau să sosească dezasamblate, în lăzi, prin Marea Nordului și apoi pe calea ferată, prin Rusia. Cel care a negociat obținerea avioanelor de vânătoare a fost colonelul Vasile Rudeanu, trimisul Ministerului de Război la Paris.</div>
<div style="text-align: justify; text-indent: 40pt; margin: 0% 8% 0% 8%">La 13 iulie 1916, Rudeanu i-a trimis o telegramă generalului de brigadă Dumitru Iliescu prin care îl anunța că vor sosi în România și doi instructori francezi: sublocotenentul René Chambe și sergentul Adam. </div>
<div style="text-align: justify; text-indent: 40pt; margin: 0% 8% 0% 8%">Cei doi au ajuns la București la 12 august, cu două zile înainte ca România să declare război Austro-Ungariei. Un al treilea aviator, sublocotenentul conte de Lareinty-Tholozan, s-a alăturat celorlalți doi piloți de vânătoare francezi (nu există date clare despre cum a sosit în România; este posibil ca în vara anului 1916 să se fi aflat, de mai mult timp, în Rusia).</div>
<div style="text-align: justify; text-indent: 40pt; margin: 0% 8% 0% 8%">Cele șase avioane Nieuport XI au fost montate până la 5 septembrie și răspândite la escadrilele de informații care sprijineau armatele noastre aflate în Transilvania și la Dunăre. Deja, câțiva piloți români învățaseră să le stăpânească. Dar, după ce aviația militară germană – venind din Bulgaria – a început să bombardeze Bucureștiul, avioanele Nieuport XI au fost reunite pe Pipera în cadrul nou-formatei Escadrile de Luptă. Acesta este începutul aviației de vânătoare române.</div>
<div style="text-align: justify; text-indent: 40pt; margin: 0% 8% 0% 8%">Escadrila de Luptă s-a înființat în luna septembrie 1916 (data exactă nu este, pentru moment, cunoscută). Era formată din cele șase avioane Nieuport XI și din șapte aviatori, cei trei francezi amintiți deja și încă patru români: căpitanul Constantin Beroniade, căpitanul Nicolae Capșa, sublocotenentul Vasile Craiu și sublocotenentul Petre Macavei. S-a stabilit ca trei avioane să fie folosite de francezi, iar celelalte trei de către români (prin rotație, un aviator român rămânea la aerodrom).</div>
<div style="text-align: justify; text-indent: 40pt; margin: 0% 8% 0% 8%">Din păcate, încă de la început au existat tensiuni între francezi și români. La originea acestora a stat orgoliul lui René Chambe, care tocmai fusese avansat la gradul de locotenent. El și-a dorit să fie numit comandant al Escadrilei de Luptă, dar șeful aviației militare române l-a desemnat pentru această funcție pe căpitanul Beroniade.</div>
<div style="text-align: justify; text-indent: 40pt; margin: 0% 8% 0% 8%">Chambe s-a supărat și a plecat de la aerodrom. S-a creat situația penibilă ca, la alarme, să decoleze numai cinci avioane, în timp ce la sol rămâneau un avion (al locotenentului francez) și un aviator român, care nu avea dreptul să-l folosească.</div>
<div style="text-align: justify; text-indent: 40pt; margin: 0% 8% 0% 8%">Spre sfârșitul lunii septembrie, pe fondul bombardamentelor asupra Capitalei și al eșecurilor suferite de armata română în Transilvania și la Dunăre, tensiunea a atins paroxismul. În timpul unei alarme, mecanicul lui Beroniade, din neatenție, a fost lovit de elicea pornită a avionului și ucis. Exact în acel moment a apărut sublocotenentul de Lareinty-Tholozan și i-a spus: „Domnule căpitan, eu nu am gloanțe pentru mitralieră”. Lui Beroniade i s-a părut absurd. Era obligația ofițerului francez să se asigure că are tot ce-i trebuie, avionul fiind utilizat exclusiv de către francezi. Așa că i-a răspuns cu sarcasm: „Nu ai gloanțe pentru mitralieră? Atunci, du-te și te plimbă!”. În urma acestui răspuns, Tholozan și Adam s-au supărat și au plecat și ei de pe Pipera. Nu au mai revenit...</div>
<div style="text-align: justify; text-indent: 40pt; margin: 0% 8% 0% 8%">Era o criză gravă, pe care șeful aeronauticii militare române trebuia să o rezolve cu diplomație. Căpitanul Beroniade a fost numit într-o altă funcție, iar locotenentul Chambe a primit comanda Escadrilei de Luptă.</div>
<div style="text-align: justify; text-indent: 40pt; margin: 0% 8% 0% 8%">Momentul a fost depășit, iar românii nu i-au purtat pică lui René Chambe. În 1917 îl găsim, în continuare, la comanda unei escadrile de vânătoare. A luptat la Mărășești și a fost rănit, iar regina Maria s-a asigurat că va primi cele mai bune îngrijiri. După război a scris mai multe cărți inspirate din experiența luptelor aeriene. Una dintre ele – „Sous le Casque de Cuir” – povestește despre escadrila sa din România.</div>
