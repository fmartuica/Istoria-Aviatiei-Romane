<h1 style="margin: 2% 10% 0% 10%">C.T.-2 (1955)</h1>
<h2 style="margin: 0% 10% 0% 10%">Planor acrobatic de perfecțiune și performanță</h2>

<font size=4>
<div style="text-align: justify; margin: 2% 10% 0% 10%"><b>Anvergura 14,60 m, lungimea 8,00 m, suprafața portantă 15,40 m<sup>2</sup>, greutatea gol 350 kg, greutatea totală 540 kg, finețea maximă la 95 km/oră 25,00, viteza maximă admisă în picaj 300 km/oră, viteza verticală de coborâre (minimă) 0,90 m/s. </b></div>
<div style="text-align: justify; text-indent: 40pt; margin: 2% 10% 0% 10%"> Planorul C.T.-2 a fost construit la <i>Atelierele de reparații material volant</i> (A.R.M.V.-2)  de la Pipera (București), după proiectul inginerului Traian Costăchescu. Fuzelajul, de tip cocă, era învelit cu placaj. Aripile, de tip monolonjeron, erau prevăzute cu frâne aerodinamice. Diedrul și ungiul de incidență al aripilor erau reglabile la sol. Unghiul de incidență al stabilizatorului se putea regla în zbor. Ultima variantă a acestui planor, denumită C.T.-2 B, avea la aripi flapsuri de intrados, cu un orificiu circular. Trenul de aterizare avea două roți, dispuse în tandem. </div>
<div style="text-align: justify; text-indent: 40pt; margin: 0% 10% 0% 10%"> Planorul C.T.-2 putea fi echipat cu aparate de bord pentru zborul fără vizibilitate, cu instalație de oxigen pentru zborul la înălțimi mari și cu o stație radio (emisie și recepție).</div>
<div style="text-align: justify; text-indent: 40pt; margin: 0% 10% 0% 10%"> În anul 1957, inginerul Mircea Finescu, pilotând un planor C.T.-2,  a stabilit un record național de distanță, cu țel fix, acoperind distanța de 202 km (între Iași și Râmnicu-Sărat), iar în 1958 același pilot l-a prezentat în cadrul unui miting de aviație desfășurat pe aeroportul Tușino (Moscova). În străinătate, planorul C.T.-2 a mai fost prezentat la trei expoziții internaționale: în 1957 - la o expoziție industrială românească în Republica Populară Chineză, în anul 1959 - la Târgul Internațional de la Brno, iar în 1960 - la cel de la Poznan.</div>
</font>



