<h1 style="margin: 2% 10% 0% 10%">I.S.-23 A (1969)</h1>
<h2 style="margin: 0% 10% 0% 10%">Avion utilitar</h2>

<font size=4>
<div style="text-align: justify; margin: 2% 10% 0% 10%"><b>Anvergura 12,40 m, lungimea 9,10 m, înălțimea 3,60 m, suprafața portantă 19,50 m<sup>2</sup>, greutatea gol 1.440 kg, greutatea totală 2.100 kg, motorul:<i> A.L.-14 R.F.</i> 300 C.P., viteza maximă 205 km/oră, viteza de croazieră 180 km/oră, viteza minimă 85 km/oră, viteza de aterizare 68 km/oră, plafonul 4.100 m, distanța maximă de zbor 600 km, lungimea rulajului la decolare 150 m, lungimea rulajului la aterizare 100 m. </b> </div>
<div style="text-align: justify; text-indent: 40pt; margin: 2% 10% 0% 10%"> Avionul I.S.-23 A, construit de <i>Întreprinderea de construcții aeronautice</i> (Brașov), după proiectul inginerului <b>Iosif Șilimon</b>, este în întregime metalic, cu aripa de sus și cu tren de aterizare triciclu (neescamotabil). Conceput ca avion utilitar, poate fi folosit, prin adaptarea unor instalații de bord, în următoarele variante: transport de persoane (pilotul + 5 pasageri), agricol, sanitar, transport de mărfuri, remorcaj de planoare și antrenament pentru parașutiști.</div>
<div style="text-align: justify; text-indent: 40pt; margin: 0% 10% 0% 10%">Aripile, de formă trapezoidală, au pe toată lungimea bordului de atac o fantă (tip slet) comandabilă, iar la bordul de scurgere - flapsuri <i>Fowler</i>. În aripi sunt instalate două rezervoare de benzină a câte 1.000 litri. </div>
<div style="text-align: justify; text-indent: 40pt; margin: 0% 10% 0% 10%">Fuzelajul are schelet metalic (din țevi sudate) și este acoperit în întregime cu tablă de duraluminiu. Suprafețele de comandă (direcția, profundorul, eleroanele) și flapsurile au înveliș de tablă ondulată. </div>
<div style="text-align: justify; text-indent: 40pt; margin: 0% 10% 0% 10%"> Elicea metalică este tripală, cu pasul variabil. Avionul este prevăzut cu comandă dublă.</div>
<div style="text-align: justify; text-indent: 40pt; margin: 0% 10% 0% 10%">La varianta agricolă, rezervorul de substanțe chimice este instalat în interiorul fuzelajului, în spațiul din spatele scaunului pilotului. </div>
</font>

