<h1 style="margin: 2% 10% 0% 10%">I.A.R.-C.V. 11 (1930)</h1>
<h2 style="margin: 0% 10% 0% 10%">Avion de vânătoare</h2>

<font size=4>
<div style="text-align: justify; margin: 2% 10% 0% 10%"><b>Anvergura 11,50 m, lungimea 6,98 m, înălțimea 2,46 m, suprafața portantă 18,20 m<sup>2</sup>, greutate gol 1100 kg, greutatea totală 1510 kg, motorul: <i>Hispano-Suiza</i> 12 Mc 500 C.P., viteza maximă 325 km/oră, viteza la 5000 m, 302 km/oră, plafonul 10.000 m, timpul de urcare până la 5000 m, 8 min 15 s. </b> </div>
<div style="text-align: justify; text-indent: 40pt; margin: 2% 10% 0% 10%"> Primul avion românesc construit la fabrica I.A.R. din Brașov, după proiectul elaborat de inginerul <b>Elie Carafoli</b>, împreună cu inginerul francez <b>Virmoux</b>. Cu acest avion, la 9 decembrie 1931, căpitanul <b> Romeo Popescu</b> a încercat să doboare recordul mondial de viteză pe bază de 500 km (306,696 km/oră), stabilit cu un avion <i>Nieuport-Delage</i>, încă în anul 1924, de pilotul francez <b>Joseph Sadi-Lecointe</b>. Această încercare avea șanse reale de reușită, deoarece la zborurile de probă ale avionului I.A.R.-C.V. 11 fusese atinsă viteza maximă de 319 km/oră. Tentativa s-a soldat însă cu un accident tragic. Din cauza unei pene la motor, pilotul a încercat să aterizeze forțat, pe teren desfundat, lângă comuna Lehliu, dar avionul a capotat. </div>
<div style="text-align: justify; text-indent: 40pt; margin: 0% 10% 0% 10%">Acest accident a costat viața căpitanului aviator Romeo Popescu, valoros pilot-eseior, unul din așii aviației române. </div>
<div style="text-align: justify; text-indent: 40pt; margin: 0% 10% 0% 10%">În anul fabricării sale, avionul I.A.R.-C.V. 11 se plasa, ca formulă de construcție și ca performanțe de zbor, printre cele mai reușite avioane de vânătoare. </div>
</font>

