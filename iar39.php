<h1 style="margin: 2% 10% 0% 10%">I.A.R.-39 (1939)</h1>
<h2 style="margin: 0% 10% 0% 10%">Avion de recunoaștere și bombardament</h2>

<font size=4>
<div style="text-align: justify; margin: 2% 10% 0% 10%"><b> Anvergura 13,10 m, lungimea 9,60 m, înălțimea 3,99 m, suprafața portantă 40,30 m<sup>2</sup>, greutatea gol 2.177 kg, greutatea totală: varianta de bombardament 3.085 kg, varianta de recunoaștere 3.007 kg, motorul: <i>I.A.R K 14-IV C 32</i> 870 C.P., viteza de croazieră 295 km/oră, viteza maximă (la 3.500 m) 336 km/oră, plafonul 8.000 m, timpul de urcare până la 3.000 m 5 minute și 32 secunde, timpul de urcare până la 6.000 m 12 minute și 21 secunde, lungimea rulajului la aterizare 170 m.</b> </div>
<div style="text-align: justify; text-indent: 40pt; margin: 2% 10% 0% 10%"> Avionul I.A.R-39  era un biplan (cu planurile inegale) de construcție mixtă: scheletul de lemn și metal, iar învelișul din placaj și pânză. Echipajul era format din pilot, observator și trăgător.</div>
<div style="text-align: justify; text-indent: 40pt; margin: 0% 10% 0% 10%"> Fuzelajul avea scheletul format din patru lonjeroane, cu montanți și hobane de înaltă rezistență. Învelișul fuzelajului, ca și cel al planurilor, era de pânză. Elicea, bipală, era de lemn, cu extremitățile blindate cu metal. Trenul de aterizare (neescamotabil), de tip arcadă, avea amortizate oleopneumatice tip <i>T.U.-I.A.R</i>.</div>
<div style="text-align: justify; text-indent: 40pt; margin: 0% 10% 0% 10%"> Avionul I.A.R.-39 era echipat cu aparatele și instalațiile de bord necesare pentru zborul de noapte.</div>
<div style="text-align: justify; text-indent: 40pt; margin: 0% 10% 0% 10%"> La nevoie, avionul putea fi pilotat de pe locul observatorului, unde se afla o a doua manșă (demontabilă) și un palonier. Armamentul defensiv se compunea din trei mitraliere de 7,92 mm dispuse astfel: o mitralieră<b> Browning P.W.U.</b> în planul stâng, comandată de pilot, și două mitraliere <b>Rheinmetall</b>, una dispusă în turela din spate și acționată de trăgător, iar cealaltă, pentru observator, amplasată în fuzelaj, cu unghiul de tragere în jos. De asemeni, avionul I.A.R.-39 putea transporta 24 de bombe a 12 kg fiecare, fixate sub planul inferior, în lasatoare de tip I.A.R.-Barbieri. Aceste lansatoare se puteau încărca și cu 24 casete speciale, conținând câte 6 grenade fiecare.</div>
<div style="text-align: justify; text-indent: 40pt; margin: 0% 10% 0% 10%"> Avionul I.A.R.-39 poate fi considerat ca una din construcțiile cele mai reușite de fabrica din Brașov. În afara celor peste 100 de avioane construite la I.A.R., s-au mai fabricat 96 de avioane I.A.R.-39 și la fabrica S.E.T. din București.</div>
</font>

