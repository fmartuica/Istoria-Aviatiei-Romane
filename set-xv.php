<div style="text-align: justify; margin: 2% 10% 0% 10%"><b>Anvergura planului superior 9,40 m,  anvergura planului inferior 6,70 m, lungimea 7,00 m, înălțimea 3,05 m, suprafața portantă 18,65 m<sup>2</sup>, greutatea gol 1.150 kg, greutatea totală 1,550 kg, motorul: <i>Gnome-Rhone 9 Krsd</i>  500 C.P., viteza maximă 350 km/oră, viteza minimă 113 km/oră, plafonul 9.400 m, timpul de urcare până la 2.000 m, 3 minute și 10 secunde, timpul de urcare până la 7.000 m, 15 minute și 30 de secunde. </b> </div>
